-- 5. Crear políticas de acceso para los Buckets
-- Políticas para el bucket 'galeria'
CREATE POLICY "Acceso publico total a galeria" ON storage.objects FOR ALL 
USING (bucket_id = 'galeria') 
WITH CHECK (bucket_id = 'galeria');

-- Políticas para el bucket 'documentacion'
CREATE POLICY "Acceso publico total a documentacion" ON storage.objects FOR ALL 
USING (bucket_id = 'documentacion') 
WITH CHECK (bucket_id = 'documentacion');

